package constants;

public class ConstantsWrapper {
	
	private int size;
	private int num;
	
	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public ConstantsWrapper(int size, int num) {
		this.size = size;
		this.num = num;
	}
	
	public ConstantsWrapper() {}
	
	
}
